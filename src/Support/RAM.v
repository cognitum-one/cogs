//`include "A2_project_settings.vh"

module   RAM #(     //JLPL
   parameter   DEPTH = 8,
   parameter   WIDTH = 8,
   parameter   LG2D  = `LG2(DEPTH)
   )(
   output reg  [WIDTH-1 :0]   RAMo,
   input  wire [WIDTH-1 :0]   iRAM,
   input  wire [LG2D -1 :0]   aRAM,
   input  wire                eRAMn,
   input  wire                wRAMn,
   input  wire                clock
   );

reg [WIDTH-1:0] RAM [0:DEPTH-1];
always @(posedge clock)
   if (!eRAMn)
      if (!wRAMn) RAM[aRAM]  <= `tCQ iRAM;
      else        RAMo[31:0] <= `tACC RAM[aRAM];

endmodule

