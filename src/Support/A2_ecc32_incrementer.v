`ifdef   verify_ecc32_incrementer
`timescale 1ns/1ps

module t;

reg   [31:0]   count;
reg   [ 6:0]   check;
reg            reset;
reg            clock;

wire  [ 6:0]   nxchk;

initial begin
   clock = 1'b0;
   forever  #50 clock = ~clock;
   end

always @(posedge clock) begin
   if (reset) begin
      count <= #1 32'h0;
      check <= #1  7'h0;
      end
   else begin
      count <= #1  count + 32'h1;
      check <= #1  nxchk;
      end
   end

A2_ecc32_incrementer mut (
   .nxchk(nxchk),
   .count(count),
   .check(check)
   );

wire  [6:0] expect;

A2_ecc32_gen i_gen (
   .e(expect),
   .d(count + 1)
   );

wire  error = expect != nxchk;

integer i;


initial begin
   $display(" Initializing test");
   reset =  1'b0;
   repeat(10) @(posedge clock); #2
   reset =  1'b1;
   repeat(10) @(posedge clock); #2
   reset =  1'b0;
   $display(" Running test");
   for (i=0; i<=4294967295; i=i+1) begin
      @(posedge clock);
      if (error) $display(" Error at @d data = %b ",i,count);
      if (i%10000000==0) $display(". %h",i);
      end
   $display(" Test finished");
   $finish;
   end

endmodule

`endif

