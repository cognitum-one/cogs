//`timescale  1ps/1fs

module A2_RingOscillator (
   output wire             RO,
   input  wire [ 2:0]      ctrl,
   input  wire             enable
   );

wire  [16:0]   w;

localparam  trim = 1.35,      //0.5,
            base = 2.4;       //2.2;
                              //                          ps      GHz            1x         1/2      1/6
not  #base i0  (w[0],w[16]);  // 2 (9 x 2.4 + 1.35 x 0) = 43.2 => 23.1     // 2 x (9 x 2.2)           = 39.6ps => 25.25GHz   12.625   4.208333
not  #trim i1  (w[1],w[0]);   // 2 (9 x 2.4 + 1.35 x 1) = 45.9 => 21.8     // 2 x (9 x 2.2 + 0.5 x 1) = 40.6ps => 24.6GHz    12.300   4.100000
not  #trim i2  (w[2],w[1]);   // 2 (9 x 2.4 + 1.35 x 2) = 48.6 => 20.6     // 2 x (9 x 2.2 + 0.5 x 2) = 41.6ps => 24.0GHz    12.000   4.000000
not  #trim i3  (w[3],w[2]);   // 2 (9 x 2.4 + 1.35 x 3) =                  // 2 x (9 x 2.2 + 0.5 x 3) = 42.6ps => 23.5GHz    11.750   3.916666
not  #trim i4  (w[4],w[3]);   // 2 (9 x 2.4 + 1.35 x 4) =                  // 2 x (9 x 2.2 + 0.5 x 4) = 43.6ps => 22.9GHz    11.450   3.816666
not  #trim i5  (w[5],w[4]);   // 2 (9 x 2.4 + 1.35 x 5) =                  // 2 x (9 x 2.2 + 0.5 x 5) = 44.6ps => 22.4GHz    11.200   3.733333
not  #trim i6  (w[6],w[5]);   // 2 (9 x 2.4 + 1.35 x 6) =                  // 2 x (9 x 2.2 + 0.5 x 6) = 45.6ps => 21.9GHz    10.950   3.650000
not  #trim i7  (w[7],w[6]);   // 2 (9 x 2.4 + 1.35 x 7) = 62.1 => 16.1     // 2 x (9 x 2.2 + 0.5 x 7) = 46.6ps => 21.4GHz    10.700   3.566666

assign w[8] = {~w[7],w[6],~w[5],w[4],~w[3],w[2],~w[1],w[0]} >> ~ctrl[2:0];  // ctrl inverted so 0 is lowest frequency

not  #base i8  (w[09],w[08]);
not  #base i9  (w[10],w[09]);
not  #base i10 (w[11],w[10]);
not  #base i11 (w[12],w[11]);
not  #base i12 (w[13],w[12]);
nand #base i13 (w[14],w[13],enable);
not  #base i14 (w[15],w[14]);
not  #base i15 (w[16],w[15]);

assign   RO =   w[12];

endmodule

