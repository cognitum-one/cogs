module A2_ClockMux (
   output wire clocko,
   input  wire clocka,
   input  wire clockb,
   input  wire select,
   input  wire reseta,         // Reset synchronous to clock a
   input  wire resetb          // Reset synchronous to clock b
   );

reg [2:0] A,B;

always @(posedge clocka)   A[2:0] <=  {A[1:0], ~select & ~B[1] |  reseta};

always @(posedge clockb)   B[2:0] <=  {B[1:0],  select & ~A[1] & ~resetb};

assign   clocko   = (A[2] & clocka) | (B[2] & clockb);


endmodule

