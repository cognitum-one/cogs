module   A2_RPUF_CoP #(
   parameter   [12:0]   BASE = 13'h00000, //JL
   parameter   [7:0]    RANGE = 8'h40 //JL - Size 64
   )(
   input  wire [47:0]   imosi,   // Slave data in  {cIO[2:0] aIO[12:0], iIO[31:0]}
   output wire [32:0]   imiso,   // Slave data out {               IOk, IOo[31:0]}
   output wire          intpt_puf,      // PUF data ready interrupt
   output wire [47:0]   pmosi,   // Key Transfer Handshake Interface to AES_CoP
   output wire          puf_enable, //JLPL - 9_30_25
   output wire [8:0]    puf0_vector, //JLPL - 9_30_25
   output wire [8:0]    puf1_vector, //JLPL - 9_30_25
   output wire [8:0]    puf2_vector, //JLPL - 9_30_25
   output wire [8:0]    puf3_vector, //JLPL - 9_30_25
   output wire [3:0]    puf_osc_out, //JLPL - 9_30_25
   input  wire [3:0]    puf_osc, //External Oscillator Input
   input                reset,   //Active High Reset Input
   input                clock    //TRNG Interface Clock
   );

//Note Control Registers and I/O Control are at levels below the Gasket Level so the imosi/imiso are just pushed through the Gasket Level
 
// PUF LPF Top Module Instantiation -------------------------------------------------------------------------------------------------------------------------

// I/O Module Number of Registers

//`include "/proj/TekStart/lokotech/soc/users/romeo/cognitum_a0/src/include/COGNITUM_IO_addresses.vh" - Put back in later after finding ncverilog compile sensitivity with local param header COGNITUM_IO_addresses.vh

  apb_ro_puf puf_top (
    .PCLK           (clock      ),
    .PRESET         (reset      ),
    .imiso          (imiso      ),
    .imosi          (imosi      ),
    .rmosi          (pmosi      ),
    .puf_osc        (puf_osc    ),
	.puf_enable     (puf_enable ), //JLPL - 9_30_25
	.puf0_vector    (puf0_vector), //JLPL - 9_30_25
	.puf1_vector    (puf1_vector), //JLPL - 9_30_25
	.puf2_vector    (puf2_vector), //JLPL - 9_30_25
	.puf3_vector    (puf3_vector), //JLPL - 9_30_25
	.puf_osc_out    (puf_osc_out), //JLPL - 9_30_25
    .ip2intc_irpt   (intpt_puf  )
    );
 
endmodule

